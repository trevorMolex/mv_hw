package common;

public class Variables {
    private String MyWorld;
    private String HelloWorldResponse;

    public String getMyWorld() {
        return MyWorld;
    }

    public String getHelloWorldResponse() {
        return HelloWorldResponse;
    }

    public void setMyWorld(String myWorld) {
        MyWorld = myWorld;
    }

    public void setHelloWorldResponse(String helloWorldResponse) {
        HelloWorldResponse = helloWorldResponse;
    }
}
